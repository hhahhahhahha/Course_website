package com.example.second.entity;

public class Department {
    private Long uid;

    private String name;

    private Integer id;

    private String majorsnum;

    public Long getUid() {
        return uid;
    }

    public void setUid(Long uid) {
        this.uid = uid;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMajorsnum() {
        return majorsnum;
    }

    public void setMajorsnum(String majorsnum) {
        this.majorsnum = majorsnum;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }
}
